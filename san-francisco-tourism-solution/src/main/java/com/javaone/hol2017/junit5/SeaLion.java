package com.javaone.hol2017.junit5;

public class SeaLion {
	
	private int age;
	private int weight;
	private boolean male;
	
	public SeaLion(int age, int weight, boolean male) {
		super();
		this.age = age;
		this.weight = weight;
		this.male = male;
	}

	public int getAge() {
		return age;
	}

	public int getWeight() {
		return weight;
	}

	public boolean isMale() {
		return male;
	}
	

}
